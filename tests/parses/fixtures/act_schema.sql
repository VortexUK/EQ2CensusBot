CREATE TABLE encounter_table (
    encid     CHAR(8) PRIMARY KEY,
    title     VARCHAR(64),
    starttime TEXT,
    endtime   TEXT,
    duration  INT,
    damage    INTEGER,
    encdps    REAL,
    zone      VARCHAR(64),
    kills     INT,
    deaths    INT
);

CREATE TABLE combatant_table (
    encid          CHAR(8),
    ally           CHAR(1),
    name           VARCHAR(64),
    starttime      TEXT,
    endtime        TEXT,
    duration       INT,
    damage         INTEGER,
    damageperc     VARCHAR(4),
    kills          INT,
    healed         INTEGER,
    healedperc     VARCHAR(4),
    critheals      INT,
    heals          INT,
    curedispels    INT,
    powerdrain     INTEGER,
    powerreplenish INTEGER,
    dps            REAL,
    encdps         REAL,
    enchps         REAL,
    hits           INT,
    crithits       INT,
    blocked        INT,
    misses         INT,
    swings         INT,
    healstaken     INTEGER,
    damagetaken    INTEGER,
    deaths         INT,
    tohit          REAL,
    critdamperc    VARCHAR(8),
    crithealperc   VARCHAR(8),
    crittypes      VARCHAR(32),
    threatstr      VARCHAR(32),
    threatdelta    INTEGER
);

